"""
numeric type
int - whole numbers 1,2,3
float - decimal numbers 1.22,0.18
complex - imaginary numbers 3+4j
"""

a = 1
b = 3.4
c = 3+4j
print(a,b,c)
print(type(a),type(b),type(c))

"""
booleans - True/False
"""

is_raining = True
is_sunny = False
print(is_raining,is_sunny)

# None type
result = None
print(result,type(result))
